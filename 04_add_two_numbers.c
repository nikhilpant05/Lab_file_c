#include <stdio.h>

int main() {
    // Addition of two numbers
    int a;
    int b;
    int sum;

    printf("Enter the first number: ");
    scanf("%d", &a);

    printf("Enter the second number: ");
    scanf("%d", &b);

    sum = a + b;

    printf("The sum of the numbers is %d", sum);

    return 0;
}
